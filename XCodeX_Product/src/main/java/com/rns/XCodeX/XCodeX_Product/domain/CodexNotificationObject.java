package com.rns.XCodeX.XCodeX_Product.domain;

public class CodexNotificationObject {
	
	private String title;
    private String body;
    private String android_channel_id;
    private String icon;
	private String click_action;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public String getAndroid_channel_id() {
		return android_channel_id;
	}
	public void setAndroid_channel_id(String android_channel_id) {
		this.android_channel_id = android_channel_id;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getClick_action() {
		return click_action;
	}
	public void setClick_action(String click_action) {
		this.click_action = click_action;
	}
	
	

}
